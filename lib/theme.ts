export type ThemeTokens = {
  [Key in keyof typeof THEME.light]: string;
};

export function getThemeTokens(mode: ThemeMode, accent: AccentName = DEFAULT_ACCENT): ThemeTokens {
  return {
    ...THEME[mode],
    ...THEME_ACCENTS[accent][mode],
  } as ThemeTokens;
}

export function getNavTheme(mode: ThemeMode, accent: AccentName = DEFAULT_ACCENT): Theme {
  const tokens = getThemeTokens(mode, accent);
  const baseTheme = mode === "dark" ? DarkTheme : DefaultTheme;

  return {
    ...baseTheme,
    colors: {
      ...baseTheme.colors,
      background: tokens.background,
      border: tokens.border,
      card: tokens.card,
      notification: tokens.destructive,
      primary: tokens.primary,
      text: tokens.foreground,
    },
  };
}

export const NAV_THEME: Record<ThemeMode, Theme> = {
  light: getNavTheme("light"),
  dark: getNavTheme("dark"),
};
